export const T = Object.fromEntries(
  Object.keys(LANGS).map(k => {
    if (k === "en") return [k, T_EN];
    const o = T_LANGS[k] || {};
    // Shallow-merge top-level keys, but deep-merge `set` so a language that
    // translates only some settings strings still falls back to English for
    // the rest (instead of blanking the whole panel).
    return [k, { ...T_EN, ...o, set: { ...T_EN.set, ...(o.set || {}) } }];
  })
);

// Returns the translation set for a language code, falling back to English.
export function getTranslations(lang) {
  return T[lang] || T.en;
}
